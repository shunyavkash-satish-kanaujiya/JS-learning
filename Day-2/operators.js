// TYPE OF OPERATORS
// 1. ARITHMATIC OPERATORS
//     - Addition(+)
//     - Subtraction(-)
//     - Multiplication(*)
//     - Division(/)
//     - Remainder(%)
let a = 10;
let b = 5;

console.log("Addition: ", a + b);
console.log("Subtraction: ", a - b);
console.log("Multiplication: ", a * b);
console.log("Division: ", a / b);
console.log("Modulus: ", a % b);

// 2. COMPARISION OPERATORS
//     - Equal to (==)
//     - Not Equal to (!=)
//     - Greater than (>)
//     - Less than (<)
//     - Greater than or equal to (>=)
//     - Less than or equal to (<=)
let c = 10;
let d = 5;

console.log("Equal to: ", c == d);
console.log("Not equal to: ", c != d);
console.log("Greater than: ", c > d);
console.log("Less than: ", c < d);
console.log("Greater than or equal to: ", c >= d);
console.log("Less than or equal to: ", c <= d);

// 3. LOGICAL OPERATORS
//     - AND (&&)
//     - OR (||)
//     - NOT (!)
let e = true;
let f = false;

console.log("AND: ", e && f);
console.log("OR: ", e || f);
console.log("NOT: ", !e);

// 4. ASSIGNMENT OPERATORS
//     - Addition assignment (+=)
//     - Subtraction assignment (-=)
//     - Multiplication assignment (*=)
//     - Division assignment (/=)
//     - Remainder assignment (%=)
//     - Exponentiation assignment (**=)
//     - Bitwise AND assignment (&=)
//     - Bitwise OR assignment (|=)
//     - Bitwise XOR assignment (^=)
//     - Bitwise NOT assignment (~=)
let g = 10;
let h = 5;

console.log("Addition assignment: ", (g += h));
console.log("Subtraction assignment: ", (g -= h));
console.log("Multiplication assignment: ", (g *= h));
console.log("Division assignment: ", (g /= h));
console.log("Remainder assignment: ", (g %= h));
